package com;

public class User {
    private String name;
    private String email;
    private String phoneNumber;
    private String major;
    private String minor;


    public User(String name, String email, String phoneNumber, String major, String minor){
        this.name = name;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.major = major;
        this.minor = minor;

    }

}
